    def args_parse(self, args):
        # set up command-line arguments parser
        parser = ArgumentParser(description="A script to emulate an OLED display")
        parser.add_argument(
            "-t", "--txt", help="Path to the text file which will be displayed on the OLED"
        )
        parser.add_argument(
            "-o", "--out", help="Path to the output graphics file"
        )
        parser.add_argument("--oledx", type=int, default="128", help="OLED pixels in X direction, default 128")
        parser.add_argument("--oledy", type=int, default="64", help="OLED pixels in Y direction, default 64")
        parser.add_argument("--pixelx", type=int, default="4", help="OLED pixel size in Pillow pixels in X direction, default 4")
        parser.add_argument("--pixely", type=int, default="4", help="OLED pixel size in Pillow pixels in Y direction, default 4")
        parser.add_argument("--gapx", type=int, default="1", help="Gap between OLED pixels in Pillow pixels in X direction, default 1")
        parser.add_argument("--gapy", type=int, default="1", help="Gap between OLED pixels in Pillow pixels in Y direction, default 1")
        parser.add_argument("--duration", type=int, default="500", help="GIF frame duration, default 500")
        parser.add_argument("--color", type=str, default="white", help="The pixel color, default white")
        return parser.parse_args(args)

    def emulateOLED(self, args):
        """The main method

        Args:
            args - the command line args [1:] - parsed with args_parse
        """

        frames = [] # Array of image frames
        frame = None # Frame 'counter' (frame numbers don't need to be consecutive)

        try: 
            # Open the txt file
            with open(args.txt, encoding='utf-8') as fp:
                # For each line in the file
                for line in fp.read().splitlines():
                    # Split the line using tab separators
                    fields = line.split('\t')
                    # Check there are at least 4 fields - TODO: make this more robust
                    if len(fields) < 4:
                        print("Unable to parse line: " + str(fields))
                        continue
                    # If the frame number has changed
                    if (frame is None) or (frame != int(fields[1])):
                        # On the second+ pass, append the previous image to frames
                        if frame is not None:
                            frames.append(image)
                        frame = int(fields[1]) # Record the frame number
                        # Create a new image to hold the frame
                        image = Image.new("RGB", \
                        		((args.oledx * (args.pixelx + args.gapx)), \
                        		(args.oledy * (args.pixely + args.gapy))), \
                        		"black")
                        draw = ImageDraw.Draw(image)
                    
                    # Step through all the fonts
                    for font in self.FONTS:
                        # Check if the font name matches
                        if (len(fields) == 7) and (fields[0] == font['name']):
                            # This code is taken from the Qwiic OLED library
                            nRows = int(floor(font['height'] / 8))
                            if nRows == 0:
                                nRows = 1
                            margin5x7 = nRows == 1
                            
                            # Extract the x,y OLED pixel coords
                            xp = int(fields[3])
                            yp = int(fields[4])
                            
                            if int(fields[2]) == 1: # If the text is centered
                                # Move xp left by half the text width
                                xp = xp - int((len(fields[6]) / 2.0) * (font['width'] + margin5x7))
                            
                            pixelColor = args.color
                            if int(fields[5]) == 1: # If the text is inverse
                                for yi in range(yp - 1, yp + font['height'] + 1):
                                    for xi in range(xp - 1, xp + (len(fields[6]) * (font['width'] + margin5x7))):
                                        xx = xi * (args.pixelx + args.gapx)
                                        yy = yi * (args.pixely + args.gapy)
                                        points = [(xx, yy),\
                                                (xx + args.pixelx - 1, yy),\
                                                (xx + args.pixelx - 1, yy + args.pixely - 1),\
                                                (xx, yy + args.pixely - 1)]
                                        draw.polygon(points, width=1, fill=args.color, outline=args.color)
                                pixelColor = "black"
                                
                                
                            # For each character in the text
                            for c in fields[6]:
                                # Check if the character is supported
                                if (ord(c) >= font['start']) and (ord(c) < (font['start'] + font['nchar'])):
                                    # Skip \r and \n
                                    if (c == '\r') or (c == '\n'):
                                        continue
                                    
                                    # This code is taken from the Qwiic OLED library
                                    nRowLen = int(floor(font['map'] / font['width']))
                                    rowBytes = font['map'] * nRows
                                    charOffset = ord(c) - font['start']
                                    fontIndex = int((floor(charOffset / nRowLen) * rowBytes) + ((charOffset % nRowLen) * font['width']))
                                    
                                    # Walk the rows of this font entry (it can span bytes)
                                    for row in range(nRows):
                                        rowOffset = row * 8 # y offset for multi row fonts
                                        
                                        # Walk the width of the font
                                        for i in range (font['width'] + margin5x7):
                                            # This is done in a weird way because the 5x7 font has no margin
                                            if margin5x7 and (i == font['width']):
                                                continue
                                        
                                            charByte = fontIndex + i + (row * font['map'])
                                            currChar = font['data'][charByte]
                                            
                                            # Draw bits
                                            for j in range(8):
                                                if (currChar & (1 << j)):
                                                    xx = (xp + i) * (args.pixelx + args.gapx)
                                                    yy = (yp + j + rowOffset) * (args.pixely + args.gapy)
                                                    points = [(xx, yy),\
                                                            (xx + args.pixelx - 1, yy),\
                                                            (xx + args.pixelx - 1, yy + args.pixely - 1),\
                                                            (xx, yy + args.pixely - 1)]
                                                    draw.polygon(points, width=1, fill=pixelColor, outline=pixelColor)
                                xp = xp + font['width'] + margin5x7
                    
                    # Step through all the icons
                    for icon in self.ICONS:
                        if (len(fields) == 4) and (fields[0] == icon['name']):
                            # Check for finger trouble
                            if (len(icon['data']) != (icon['width'] * int(ceil(icon['height'] / 8)))):
                                print("Problem with icon: " + icon['name'])
                                continue
                            
                            # Extract the x,y OLED pixel coords
                            xp = int(fields[2])
                            yp = int(fields[3])

                            # Walk each row of bytes                            
                            for r in range(int(ceil(icon['height'] / 8))):
                                numBits = icon['height'] - (r * 8)
                                if numBits > 8:
                                    numBits = 8
                                # Walk each column
                                for c in range(icon['width']):
                                    # Walk each bit in this column
                                    for b in range(numBits):
                                        if (icon['data'][c + (r * icon['width'])] & (1 << b)):
                                                    xx = (xp + c) * (args.pixelx + args.gapx)
                                                    yy = (yp + (r * 8) + b) * (args.pixely + args.gapy)
                                                    points = [(xx, yy),\
                                                            (xx + args.pixelx - 1, yy),\
                                                            (xx + args.pixelx - 1, yy + args.pixely - 1),\
                                                            (xx, yy + args.pixely - 1)]
                                                    draw.polygon(points, width=1, fill=args.color, outline=args.color)
                
                frames.append(image)
                        
        except:
            raise RuntimeError("Unable to read " + args.txt)

        try:
            frames[0].save(args.out, save_all=True, append_images=frames[1:], optimize=False, duration=args.duration, loop=0)
        except:
            raise RuntimeError("Unable to save " + args.out)


