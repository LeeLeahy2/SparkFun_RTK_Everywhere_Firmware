#
# This is the complex version:
# It supports the 5x7 and 8X16 fonts from the Qwiic OLED library
# It supports icons and animation
#
# To see all the args: python OLED_Emulator_2.py -h
#
# Use: python OLED_Emulator_2.py -t test2.txt -o OLED.gif
#
# test.txt contains the definition of what you want to display on the OLED
# The OLED graphic will be written to OLED.gif. Gif is needed for animation
#
# *** Each line in the txt file must use tab separators (\t). Spaces will not work ***
#
# The format of each line in the txt file is:
#
# To display a character or a line of text:
# 5X7	0	1	2	3	4	Text
# "5X7" is the font
# "0" is the frame number for the animation
# "1" indicates if the text is centered:
#     0: not centered (coordinates are top-left)
#     1: centered (coordinates are top-center)
# "2" is the X pixel of the: top-left corner of the first character (not centered)
#                            top-middle of the middle character (centered)
# "3" is the Y pixel of the: top-left corner of the first character (not centered)
#                            top-middle of the middle character (centered)
# "4" indicates is the text is inverted: 0: not inverted, 1: inverted
#
# To display an icon:
# BT	0	1	2
# "BT" is the name of the icon
# "0" is the frame number for the animation
# "1" is the X pixel of the top-left corner of the icon
# "2" is the Y pixel of the top-left corner of the icon
#
# To create blinking text with a blinking icon:
# 5X7	0	1	64	28	0	Hello world
# 5X7	1	1	64	28	1	Hello world
# BT	1	62	40
#
